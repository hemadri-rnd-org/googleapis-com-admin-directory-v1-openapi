func CreateDirectory_orgunits_listTool(cfg *config.APIConfig) models.Tool {
	tool := mcp.NewTool("get_admin_directory_v1_customer_customerId_orgunits",
		mcp.WithDescription("Retrieves a list of all organizational units for an account."),
		mcp.WithString("customerId", mcp.Required(), mcp.Description("The unique ID for the customer's Google Workspace account. As an account administrator, you can also use the `my_customer` alias to represent your account's `customerId`. The `customerId` is also returned as part of the [Users resource](/admin-sdk/directory/v1/reference/users).")),
		mcp.WithString("orgUnitPath", mcp.Description("The full path to the organizational unit or its unique ID. Returns the children of the specified organizational unit.")),
		mcp.WithString("type", mcp.Description("Whether to return all sub-organizations or just immediate children.")),
	)

	return models.Tool{
		Definition: tool,
		Handler:    Directory_orgunits_listHandler(cfg),
	}
}
